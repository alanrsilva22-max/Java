public class Tabuada {
    public static void main(String[] args) {
        int numero = 157;
        int tabuada;
        for(int i=1; i<=10; i++){
            tabuada = numero * i;
            System.out.println(numero + " x " + i + " = " + tabuada+"\n");
        }
        

    }
    






}
